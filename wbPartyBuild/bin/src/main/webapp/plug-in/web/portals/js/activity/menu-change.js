 $(function(){
        $('.sidemenu-tt .icon_fold').click(toggleMenu);
        $('.sidemenu-item').click(addActive);
      });
      //伸缩菜单
      function toggleMenu(){
        $(this).closest('.sidemenu-menu').delay().toggleClass('active');
      }
      function addActive(){
        $('.sidemenu-item').removeClass('active');
        $(this).addClass('active');
      }
    
    
   