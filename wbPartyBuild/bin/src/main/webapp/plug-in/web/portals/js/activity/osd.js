$(function() {
	$('.node').mouseenter(showinfo);
	$('.node').mouseleave(hideinfo);
});

//显示已隐藏职位列表
function showinfo() {
	$(this).find('.node-info').show();
	$(this).addClass('active');
}
//隐藏职位列表
function hideinfo() {
	$(this).find('.node-info').hide();
	$(this).removeClass('active');
}