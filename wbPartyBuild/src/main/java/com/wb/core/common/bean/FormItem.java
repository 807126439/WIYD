package com.wb.core.common.bean;

public class FormItem {

	
	private String name;
	private Integer value;
	private String on;
	private String other;
	
	public FormItem(){}
	
	
	
	

	public FormItem(String name, Integer value, String on) {
		super();
		this.name = name;
		this.value = value;
		this.on = on;
	}



	public FormItem(String name, Integer value, String on, String other) {
		super();
		this.name = name;
		this.value = value;
		this.on = on;
		this.other = other;
	}





	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}
	public String getOn() {
		return on;
	}
	public void setOn(String on) {
		this.on = on;
	}

	public String getOther() {
		return other;
	}

	public void setOther(String other) {
		this.other = other;
	}
	
	
	
}
