package com.wb.core.common.bean;



public class MultiPramWithPage<M,T> {

	private M parmTemp;
	private Page<T> page;
	
	public MultiPramWithPage(){}
	
	
	public MultiPramWithPage(M parmTemp,Page<T> page){
		this.parmTemp=parmTemp;
		this.page=page;
		
	}
	
	

	public M getParm() {
		return parmTemp;
	}
	public void setParm(M parm) {
		this.parmTemp = parm;
	}
	public Page<T> getPage() {
		return page;
	}
	public void setPage(Page<T> page) {
		this.page = page;
	}
	
	
	
	
}
