package com.wb.web.portals.dto.accessRecord;

import java.util.Date;

public class AccessRecordDTO {
	
	private Long id;
	private Long totalAccess;
	private Long todayAccess;
	private Date lastOperatorTime;
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getTotalAccess() {
		return totalAccess;
	}
	public void setTotalAccess(Long totalAccess) {
		this.totalAccess = totalAccess;
	}
	public Long getTodayAccess() {
		return todayAccess;
	}
	public void setTodayAccess(Long todayAccess) {
		this.todayAccess = todayAccess;
	}
	public Date getLastOperatorTime() {
		return lastOperatorTime;
	}
	public void setLastOperatorTime(Date lastOperatorTime) {
		this.lastOperatorTime = lastOperatorTime;
	}
	
	
	
}
