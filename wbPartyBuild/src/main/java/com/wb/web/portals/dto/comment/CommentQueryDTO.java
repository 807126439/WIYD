package com.wb.web.portals.dto.comment;

import java.util.Date;

import com.wb.core.common.dto.BaseQueryDTO;

public class CommentQueryDTO extends BaseQueryDTO{
	
	private Long communicationId; //议题Id
	private String username;
	private Date beginDate;
	private Date endDate;
	
	
	private Date commentTimeBegin;
	private Date commentTimeEnd;
	private String departmentIds;//所属部门ID
	
	
	public Long getCommunicationId() {
		return communicationId;
	}

	public void setCommunicationId(Long communicationId) {
		this.communicationId = communicationId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public Date getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public Date getCommentTimeBegin() {
		return commentTimeBegin;
	}

	public void setCommentTimeBegin(Date commentTimeBegin) {
		this.commentTimeBegin = commentTimeBegin;
	}

	public Date getCommentTimeEnd() {
		return commentTimeEnd;
	}

	public void setCommentTimeEnd(Date commentTimeEnd) {
		this.commentTimeEnd = commentTimeEnd;
	}

	public String getDepartmentIds() {
		return departmentIds;
	}

	public void setDepartmentIds(String departmentIds) {
		this.departmentIds = departmentIds;
	}
	
			
}
