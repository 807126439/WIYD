package com.wb.web.portals.dto.manuscript;

import java.util.Date;

import com.wb.core.common.dto.BaseQueryDTO;

public class ManuscriptQueryDTO extends BaseQueryDTO{
	
	private Long activityId;
	private Date createTime;
	private Long banChunkId;
	private String title;
	private String author;
	private Short status;
	private Boolean queryByTime;
	private Boolean queryByLove;
	
	

	public Long getActivityId() {
		return activityId;
	}

	public void setActivityId(Long activityId) {
		this.activityId = activityId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Long getBanChunkId() {
		return banChunkId;
	}

	public void setBanChunkId(Long banChunkId) {
		this.banChunkId = banChunkId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public Boolean getQueryByTime() {
		return queryByTime;
	}

	public void setQueryByTime(Boolean queryByTime) {
		this.queryByTime = queryByTime;
	}

	public Boolean getQueryByLove() {
		return queryByLove;
	}

	public void setQueryByLove(Boolean queryByLove) {
		this.queryByLove = queryByLove;
	}

	public Short getStatus() {
		return status;
	}

	public void setStatus(Short status) {
		this.status = status;
	}
	
	
	
	

}
