package com.wb.web.workflow.dto.attachment;

import java.util.Date;

public class AttachmentDTO {

	private Long id;
	private String name;				//附件名称
	private String description; 		//描述
	private Short type;					//类型
	private Long processInstanceId;	 	//流程实例id
	private Long taskId; 			 	//任务id
	private Date createTime;		 	//创建时间
	private Boolean isThisTask = false;
	private Short status;	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Short getType() {
		return type;
	}
	public void setType(Short type) {
		this.type = type;
	}
	public Long getProcessInstanceId() {
		return processInstanceId;
	}
	public void setProcessInstanceId(Long processInstanceId) {
		this.processInstanceId = processInstanceId;
	}
	public Long getTaskId() {
		return taskId;
	}
	public void setTaskId(Long taskId) {
		this.taskId = taskId;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Boolean getIsThisTask() {
		return isThisTask;
	}
	public void setIsThisTask(Boolean isThisTask) {
		this.isThisTask = isThisTask;
	}
	public Short getStatus() {
		return status;
	}
	public void setStatus(Short status) {
		this.status = status;
	}
	
	
	
	
	
}
