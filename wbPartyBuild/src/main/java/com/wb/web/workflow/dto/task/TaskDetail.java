package com.wb.web.workflow.dto.task;

import java.util.Date;

import org.codehaus.jackson.annotate.JsonIgnore;

public class TaskDetail {

	
	private Long id;
	private String name;							//任务名称
	private String taskExplain;						//任务说明
	private String owner;							//发起人
	private Date createTime;						//创建时间
	private Date dealTime;							//执行时间
	private Short status;							//状态 0：未完成  1:已完成
	private Date beginTime;							//流程开始时间
	private String handler;							//任务处理者
	private String processName;						//流程名称
	private String currNode;						//当前环节
	private String nodeId;							//当前环节节点id
	private Long procInstId;						//流程实例id
	private Long procExeId;							//流程执行点id
	private String ownerTaskIds;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTaskExplain() {
		return taskExplain;
	}
	public void setTaskExplain(String taskExplain) {
		this.taskExplain = taskExplain;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getDealTime() {
		return dealTime;
	}
	public void setDealTime(Date dealTime) {
		this.dealTime = dealTime;
	}
	public Short getStatus() {
		return status;
	}
	public void setStatus(Short status) {
		this.status = status;
	}
	public Date getBeginTime() {
		return beginTime;
	}
	public void setBeginTime(Date beginTime) {
		this.beginTime = beginTime;
	}
	public String getHandler() {
		return handler;
	}
	public void setHandler(String handler) {
		this.handler = handler;
	}
	public String getProcessName() {
		return processName;
	}
	public void setProcessName(String processName) {
		this.processName = processName;
	}
	public String getCurrNode() {
		return currNode;
	}
	public void setCurrNode(String currNode) {
		this.currNode = currNode;
	}
	
	@JsonIgnore
	public String getNodeId() {
		return nodeId;
	}
	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}
	public Long getProcInstId() {
		return procInstId;
	}
	public void setProcInstId(Long procInstId) {
		this.procInstId = procInstId;
	}
	public Long getProcExeId() {
		return procExeId;
	}
	public void setProcExeId(Long procExeId) {
		this.procExeId = procExeId;
	}
	public String getOwnerTaskIds() {
		return ownerTaskIds;
	}
	public void setOwnerTaskIds(String ownerTaskIds) {
		this.ownerTaskIds = ownerTaskIds;
	}
	
	
	
	
	
	
}
